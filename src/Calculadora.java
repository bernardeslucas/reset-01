public class Calculadora {

    public int soma(int valorUm, int valorDois) {
        return valorUm + valorDois;
    }

    public int subtrai(int valorUm, int valorDois) {
        return valorUm - valorDois;
    }

    public int multiplica(int valorUm, int valorDois) {
        return valorUm * valorDois;
    }

    public int divide(int valorUm, int valorDois) {
        return valorUm / valorDois;
    }

}
